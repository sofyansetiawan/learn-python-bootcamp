print(3 > 2 and 4 > 2) # keduanya True -> True

print(3 > 2 and 2 > 4) # salah satu False -> False

print(3 > 2 and 4 > 2 and 2 > 1) # lebih dari 2 kondisi

print(3 > 2 or 2 > 2) # minimal salah 1 True -> True

print(3 > 4 or 2 > 3) # Semua False -> False

print(not 3 > 2) # Hasilnya True tapi di negasi -> False

print(not 2 == "Sofyan") # Hasilnya False tapi di negasi -> True